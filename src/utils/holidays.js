// 한국 양력 고정 공휴일 (MM-DD)
const FIXED_HOLIDAYS = [
  '01-01', // 신정
  '03-01', // 삼일절
  '05-05', // 어린이날
  '06-06', // 현충일
  '08-15', // 광복절
  '10-03', // 개천절
  '10-09', // 한글날
  '12-25', // 크리스마스
]

// 음력 기반 공휴일 (설날, 추석) — 연도별 양력 변환 테이블
// 설날: 음력 1/1 ± 1일 (3일간), 추석: 음력 8/15 ± 1일 (3일간), 부처님오신날: 음력 4/8
