/**
 * 주어진 날짜가 업무일인지 확인 (주말, 공휴일 제외)
 * @param {Date} date
 * @returns {boolean}
 */
export function isWorkday(date) {
  const day = date.getDay()
  if (day === 0 || day === 6) return false // 주말

  const mm = String(date.getMonth() + 1).padStart(2, '0')
  const dd = String(date.getDate()).padStart(2, '0')
  const mmdd = `${mm}-${dd}`

  // 양력 고정 공휴일
  if (FIXED_HOLIDAYS.includes(mmdd)) return false

  // 음력 기반 공휴일
  const year = date.getFullYear()
  const lunar = LUNAR_HOLIDAYS[year]
  if (lunar && lunar.includes(mmdd)) return false

  return true
}
